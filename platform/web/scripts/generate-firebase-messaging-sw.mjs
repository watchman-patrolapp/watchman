/**
 * Writes public/firebase-messaging-sw.js from VITE_FIREBASE_* env vars.
 * Run before `vite dev` / `vite build` so the file is never committed with real keys.
 * Vercel: set the same variables in Project → Settings → Environment Variables.
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const outPath = path.join(root, 'public', 'firebase-messaging-sw.js');

function loadEnvFile(filePath) {
  try {
    const raw = fs.readFileSync(filePath, 'utf8');
    for (const line of raw.split('\n')) {
      const trimmed = line.trim();
      if (!trimmed || trimmed.startsWith('#')) continue;
      const eq = trimmed.indexOf('=');
      if (eq <= 0) continue;
      const key = trimmed.slice(0, eq).trim();
      let val = trimmed.slice(eq + 1).trim();
      if (
        (val.startsWith('"') && val.endsWith('"')) ||
        (val.startsWith("'") && val.endsWith("'"))
      ) {
        val = val.slice(1, -1);
      }
      if (process.env[key] === undefined || process.env[key] === '') {
        process.env[key] = val;
      }
    }
  } catch {
    /* optional file */
  }
}

// Local dev: .env files are not auto-loaded for plain node
loadEnvFile(path.join(root, '.env'));
loadEnvFile(path.join(root, '.env.local'));
loadEnvFile(path.join(root, '.env.production'));
loadEnvFile(path.join(root, '.env.production.local'));

const firebaseConfig = {
  apiKey: process.env.VITE_FIREBASE_API_KEY || '',
  authDomain: process.env.VITE_FIREBASE_AUTH_DOMAIN || '',
  projectId: process.env.VITE_FIREBASE_PROJECT_ID || '',
  storageBucket: process.env.VITE_FIREBASE_STORAGE_BUCKET || '',
  messagingSenderId: process.env.VITE_FIREBASE_MESSAGING_SENDER_ID || '',
  appId: process.env.VITE_FIREBASE_APP_ID || '',
};

const src = `/**
 * Firebase Cloud Messaging — background handler (Web / PWA).
 * This file is generated by scripts/generate-firebase-messaging-sw.mjs from VITE_FIREBASE_*.
 * Do not edit; do not commit real keys — use .env / Vercel env vars.
 */
importScripts('https://www.gstatic.com/firebasejs/10.12.0/firebase-app-compat.js');
importScripts('https://www.gstatic.com/firebasejs/10.12.0/firebase-messaging-compat.js');

const firebaseConfig = ${JSON.stringify(firebaseConfig, null, 2)};

const WEB_PUSH_CONFIGURED =
  firebaseConfig.appId && !String(firebaseConfig.appId).includes('REPLACE');

if (WEB_PUSH_CONFIGURED) {
  firebase.initializeApp(firebaseConfig);
  const messaging = firebase.messaging();

  messaging.onBackgroundMessage((payload) => {
    const data = payload.data || {};
    const n = payload.notification || {};
    const title = n.title || data.title || 'Neighbourhood Watch';
    const body = n.body || data.body || 'New activity in chat';
    const icon = n.icon || data.icon || '/assets/icons/icon-192.webp';
    const roomId = data.roomId || data.room_id || null;

    self.registration.showNotification(title, {
      body,
      icon,
      badge: '/assets/icons/icon-192.webp',
      tag: roomId || 'neighbourhood-watch-chat',
      data: { ...data, roomId },
      actions: [
        { action: 'open', title: 'Open chat' },
        { action: 'dismiss', title: 'Dismiss' },
      ],
    });
  });
}

self.addEventListener('notificationclick', (event) => {
  event.notification.close();
  if (event.action === 'dismiss') return;

  const data = event.notification.data || {};
  const roomId = data.roomId || data.room_id;
  const qs = roomId ? \`?room=\${encodeURIComponent(roomId)}\` : '';
  const url = \`\${self.location.origin}/chat\${qs}\`;

  event.waitUntil(
    self.clients.matchAll({ type: 'window', includeUncontrolled: true }).then((clientList) => {
      for (const client of clientList) {
        if (client.url.startsWith(self.location.origin) && 'focus' in client) {
          client.focus();
          client.postMessage({ type: 'NAVIGATE_TO_CHAT', roomId: roomId || null });
          return;
        }
      }
      return self.clients.openWindow(url);
    })
  );
});
`;

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, src, 'utf8');
if (firebaseConfig.appId) {
  console.log('[generate-firebase-messaging-sw] wrote public/firebase-messaging-sw.js (FCM configured)');
} else {
  console.warn(
    '[generate-firebase-messaging-sw] wrote stub public/firebase-messaging-sw.js (set VITE_FIREBASE_* for push)'
  );
}
